// split //
var x1 = "I love programming";
document.write(x1.split(" ") + "<br>") // split the words with ,

// charAt // 
var x2 = "Ahmed"
document.write(x2.charAt(1) + "<br>")  // find the character at index[]

// concat //
var x3= "Ahmed"
var concatination = " Hussein"
document.write(x3.concat(concatination) + "<br>") // joining 2 variables together

// length // 
var x4 = "Ahmed Hussein Alfaisal Ahmed Mohamed Saber";
document.write(x4.length + "<br>")

