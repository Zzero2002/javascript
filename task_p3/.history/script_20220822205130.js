// split //
var x1 = "I love programming";
document.write(x1.split(" ") + "<br>") // split the words with ,

// charAt // 
var x2 = "Ahmed"
document.write(x2.charAt(1) + "<br>")  // find the character at index[]

// concat //
var x3= "Ahmed"
var concatination = " Hussein"
document.write(x3.concat(concatination) + "<br>") // joining 2 variables together

// length // 
var x4 = "Ahmed Hussein Alfaisal Ahmed Mohamed Saber";
document.write(x4.length + "<br>") // count the length of string

// replace //

var x5 = "Ahmed Hussein Alfaisal Ahmed"
document.write(x5.replace("Hussein","MOahmed") + "<br>") // replace hussein with mohamed

// indexof //
var x6 = "ahmed hussein alfaisal";
document.write(x6.indexOf("hu") + "<br>") // getting first hu

// last index of//
var x7 = "ahmed hussein alfaisal"
document.write(x7.lastIndexOf("a") + "<br>") // getting last a

// to lower case // 
var x8 = "EGYPT";
document.write(x8.toLowerCase() + "<br>") // converting word to lowercase

// to upper case // 
var x9 = "egypt"
document.write(x9.toUpperCase() + "<br>") // converting word to uppercase

